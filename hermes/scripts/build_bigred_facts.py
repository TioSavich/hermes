#!/usr/bin/env python3
"""Convert Big Red's 2026-07-22 collected results into served Prolog facts.

The collected directory is read-only input. Regenerate the two output files
after a new collected run with:

    python3 hermes/scripts/build_bigred_facts.py
"""

from __future__ import annotations

import json
from pathlib import Path


REPO = Path(__file__).resolve().parents[2]
COLLECTED = REPO / ".bigred-collected/2026-07-22"
CARVING_INPUT = COLLECTED / "predicate-carving/predicate-carving.json"
HYPEREDGE_INPUT = COLLECTED / "hyperedges/hyperedges.json"
CARVING_OUTPUT = REPO / "curriculum/im/generated/predicate_carving_facts.pl"
HYPEREDGE_OUTPUT = REPO / "knowledge/hyperedges/bigred_emergent_hyperedges.pl"
RUN_DATE = "2026-07-22"


def prolog_string(value: str) -> str:
    """Emit a SWI-Prolog string literal without interpreting collected text."""
    return json.dumps(value, ensure_ascii=False)


def write(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    temporary = path.with_suffix(path.suffix + ".tmp")
    temporary.write_text(text, encoding="utf-8")
    temporary.replace(path)


def build_carving(data: dict) -> str:
    lessons = data["lessons"]
    operations = data["operations"]
    verdicts = data["containment_verdicts"]
    lines = [
        "/** <module> Generated Big Red predicate-carving facts\n",
        " *\n",
        " * Generated by hermes/scripts/build_bigred_facts.py from the read-only\n",
        " * .bigred-collected/2026-07-22/predicate-carving/predicate-carving.json.\n",
        " * The lesson reports are served facts; the inferential-strength module\n",
        " * retains its live computation as a fallback when this file is absent.\n",
        " */\n",
        ":- module(predicate_carving_facts,\n",
        "          [ precomputed_lesson_inferential_strength/2,\n",
        "            precomputed_operation_path_model/3,\n",
        "            precomputed_containment_verdict/4,\n",
        "            predicate_carving_provenance/4\n",
        "          ]).\n\n",
        f"predicate_carving_provenance({prolog_string(RUN_DATE)}, {prolog_string(data['scenario'])}, {prolog_string(data['scope'])}, {data['limit']}).\n\n",
    ]
    for row in lessons:
        lesson = row["lesson"]
        if not (lesson.startswith("'") and lesson.endswith("'")):
            raise ValueError(f"unexpected lesson term: {lesson!r}")
        lines.append(
            f"precomputed_lesson_inferential_strength({lesson}, {row['report']}).\n"
        )
    lines.append("\n")
    for row in operations:
        lines.append(
            "precomputed_operation_path_model("
            f"{prolog_string(row['operation'])}, {prolog_string(row['path_model'])}, {row['registered_signatures']}).\n"
        )
    lines.append("\n")
    for row in verdicts:
        lines.append(
            "precomputed_containment_verdict("
            f"{prolog_string(row['from'])}, {prolog_string(row['to'])}, "
            f"{str(row['entails']).lower()}, {prolog_string(row['witness'])}).\n"
        )
    return "".join(lines)


def build_hyperedges(data: dict) -> str:
    lines = [
        "/** <module> Generated Big Red emergent incompatibility sets\n",
        " *\n",
        " * Generated by hermes/scripts/build_bigred_facts.py from the read-only\n",
        " * .bigred-collected/2026-07-22/hyperedges/hyperedges.json.\n",
        " */\n",
        ":- module(bigred_emergent_hyperedges,\n",
        "          [ bigred_emergent_hyperedge/4,\n",
        "            bigred_hyperedge_provenance/5\n",
        "          ]).\n\n",
        "bigred_hyperedge_provenance("
        f"{prolog_string(RUN_DATE)}, {prolog_string(data['scenario'])}, "
        f"{prolog_string(data['scope'])}, {data['limit']}, {data['total_emergent_sets']}).\n\n",
    ]
    for set_text in data["emergent_sets"]:
        if not (set_text.startswith("[") and set_text.endswith("]")):
            raise ValueError(f"unexpected hyperedge set: {set_text!r}")
        lines.append(
            "bigred_emergent_hyperedge("
            f"{prolog_string(RUN_DATE)}, {prolog_string(data['scenario'])}, "
            f"{prolog_string(data['scope'])}, {set_text}).\n"
        )
    return "".join(lines)


def main() -> int:
    carving = json.loads(CARVING_INPUT.read_text(encoding="utf-8"))
    hyperedges = json.loads(HYPEREDGE_INPUT.read_text(encoding="utf-8"))
    if len(carving["containment_verdicts"]) != 18_632:
        raise ValueError("unexpected containment verdict count")
    if len(carving["lessons"]) != 1_313 or len(carving["operations"]) != 13:
        raise ValueError("unexpected predicate-carving inventory")
    write(CARVING_OUTPUT, build_carving(carving))
    write(HYPEREDGE_OUTPUT, build_hyperedges(hyperedges))
    print(f"wrote {CARVING_OUTPUT.relative_to(REPO)}: lessons={len(carving['lessons'])} operations={len(carving['operations'])} verdicts={len(carving['containment_verdicts'])}")
    print(f"wrote {HYPEREDGE_OUTPUT.relative_to(REPO)}: emergent_sets={len(hyperedges['emergent_sets'])}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
