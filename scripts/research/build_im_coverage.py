#!/usr/bin/env python3
"""Build the per-lesson Illustrative Mathematics coverage inventory."""

from __future__ import annotations

import json
import re
import subprocess
import sys
from collections import Counter, defaultdict
from pathlib import Path


REPO = Path(__file__).resolve().parents[2]
RATCHET_PATH = Path(
    "/Users/tio/Documents/GitHub/umedcta-formalization/"
    "scripts/curriculum/lesson_evidence_ratchet.json"
)
JSON_OUTPUT = REPO / "curriculum/im/coverage/im_coverage.json"
SUMMARY_OUTPUT = REPO / "curriculum/im/coverage/im_coverage_summary.md"
GENERATED_HEADER = (
    "generated by scripts/research/build_im_coverage.py — do not hand-edit"
)
LESSON_RE = re.compile(r"^IM-G(K|[1-8])-U(\d+)-L(\d+)$")


PROLOG_GOAL = r"""
use_module(lessons('im/lesson_monitoring')),
use_module(lessons('im/generated/compiled_task_instances')),
use_module(lessons('im/generated/vision_lesson_digest')),
findall(C,lesson_monitoring:im_lesson(C,_,_,_,_,_),P0),sort(P0,P),
forall(member(C,P),format('published\t~w~n',[C])),
forall((clause(text_interpreter:explicit_lesson_text_source(C,_),_),memberchk(C,P)),format('text\t~w~n',[C])),
forall((clause(lesson_monitoring:explicit_lesson_strategy(C,O,K,_),_),memberchk(C,P)),format('hand\t~w\t~w\t~w~n',[C,O,K])),
forall((compiled_action_mappings:compiled_lesson_strategy(C,O,K,_),memberchk(C,P)),format('compiled\t~w\t~w\t~w~n',[C,O,K])),
forall((clause(lesson_monitoring:explicit_lesson_misconception(C,_,_,_),_),memberchk(C,P)),format('misconception\t~w~n',[C])),
forall((compiled_lesson_context:compiled_lesson_context(C,_,_,_),memberchk(C,P)),format('context\t~w~n',[C])),
forall((compiled_task_instances:compiled_lesson_task_instance(C,T,_),memberchk(C,P),(T=deformation(_)-_ -> D=deformation ; D=task)),format('task\t~w\t~w~n',[C,D])),
forall((vision_lesson_digest:vision_lesson(C,_,_,_,_),memberchk(C,P)),format('vision\t~w~n',[C])),
forall(vision_lesson_digest:vision_lesson(C,_,_,_,_),format('vision_all\t~w~n',[C])),
forall((member(C,P),lesson_monitoring:im_lesson(C,Concept,_,_,_,_),atom_string(C,CS),once(lesson_monitoring:loaded_standard_anchor(Concept,im_lesson,CS,_))),format('standard_lesson\t~w~n',[C])),
forall((member(C,P),lesson_monitoring:im_lesson(C,Concept,_,_,_,_),lesson_monitoring:lesson_unit_anchor(C,U),atom_string(U,US),once(lesson_monitoring:loaded_standard_anchor(Concept,im_lesson,US,_))),format('standard_unit\t~w~n',[C])),
forall(action_automata_registry:action_automaton_signature(_,K,_,_),format('signature\t~w~n',[K])),
halt
""".replace("\n", " ")


def fail(message: str) -> "NoReturn":
    print(f"build_im_coverage.py: {message}", file=sys.stderr)
    raise SystemExit(1)


def load_ratchet() -> dict:
    if not RATCHET_PATH.is_file():
        fail(f"required dependency is absent: {RATCHET_PATH}")
    try:
        data = json.loads(RATCHET_PATH.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError) as exc:
        fail(f"cannot read required dependency {RATCHET_PATH}: {exc}")
    if data.get("schema") != "im_lesson_evidence_ratchet_v1":
        fail(
            f"unexpected schema in {RATCHET_PATH}: "
            f"{data.get('schema')!r}"
        )
    return data


def prolog_facts() -> dict[str, list[list[str]]]:
    command = ["swipl", "-q", "-l", "paths.pl", "-g", PROLOG_GOAL]
    try:
        result = subprocess.run(
            command,
            cwd=REPO,
            check=True,
            capture_output=True,
            text=True,
        )
    except FileNotFoundError:
        fail("swipl is required to read the loaded Hermes lesson stack")
    except subprocess.CalledProcessError as exc:
        detail = exc.stderr.strip() or exc.stdout.strip() or "no diagnostic"
        fail(f"swipl lesson query failed: {detail}")

    facts: dict[str, list[list[str]]] = defaultdict(list)
    for line in result.stdout.splitlines():
        fields = line.split("\t")
        if fields and fields[0]:
            facts[fields[0]].append(fields[1:])
    return facts


def lesson_parts(lesson: str) -> tuple[str, int, int]:
    match = LESSON_RE.fullmatch(lesson)
    if not match:
        fail(f"invalid IM lesson id from loaded facts: {lesson}")
    return match.group(1), int(match.group(2)), int(match.group(3))


def regex_ids(path: Path, predicate: str) -> set[str]:
    pattern = re.compile(rf"^{re.escape(predicate)}\('([^']+)'", re.MULTILINE)
    return set(pattern.findall(path.read_text(encoding="utf-8")))


def regex_cross_checks(runtime: dict[str, set[str]]) -> list[dict[str, int | str]]:
    grade_files = sorted((REPO / "curriculum/im").glob("grade_*.pl"))
    monitoring = REPO / "curriculum/im/lesson_monitoring.pl"
    checks: list[tuple[str, set[str]]] = []

    text_ids: set[str] = set()
    hand_ids: set[str] = set()
    misconception_ids: set[str] = set()
    for path in grade_files:
        text_ids |= regex_ids(path, "text_interpreter:explicit_lesson_text_source")
        hand_ids |= regex_ids(path, "explicit_lesson_strategy")
        misconception_ids |= regex_ids(path, "explicit_lesson_misconception")
    hand_ids |= regex_ids(monitoring, "explicit_lesson_strategy")
    misconception_ids |= regex_ids(monitoring, "explicit_lesson_misconception")

    checks.extend(
        [
            ("text_source", text_ids),
            ("hand_strategy", hand_ids),
            ("misconception_link", misconception_ids),
            (
                "compiled_strategy",
                regex_ids(
                    REPO / "curriculum/im/generated/compiled_action_mappings.pl",
                    "compiled_lesson_strategy",
                ),
            ),
            (
                "verbatim_context",
                regex_ids(
                    REPO / "curriculum/im/generated/compiled_lesson_context.pl",
                    "compiled_lesson_context",
                ),
            ),
            (
                "vision_digest",
                regex_ids(
                    REPO / "curriculum/im/generated/vision_lesson_digest.pl",
                    "vision_lesson",
                ),
            ),
        ]
    )

    published = runtime["published"]
    evidence = []
    for layer, parsed_ids in checks:
        parsed_published = parsed_ids & published
        loaded_ids = runtime[layer]
        if parsed_published != loaded_ids:
            only_regex = sorted(parsed_published - loaded_ids)[:5]
            only_swipl = sorted(loaded_ids - parsed_published)[:5]
            fail(
                f"SWI-Prolog/regex mismatch for {layer}: "
                f"regex-only={only_regex}, swipl-only={only_swipl}"
            )
        evidence.append(
            {"layer": layer, "swipl": len(loaded_ids), "regex": len(parsed_published)}
        )
    return evidence


def render_ids() -> dict[str, set[str]]:
    generated = REPO / "hermes/app/web/generated"
    notation = {
        path.name
        for path in (generated / "notation_lesson_charts").glob("IM-*")
        if path.is_dir() and LESSON_RE.fullmatch(path.name)
    }
    deformation = {
        path.name
        for path in (generated / "lesson_deformation_charts").glob("IM-*")
        if path.is_dir() and LESSON_RE.fullmatch(path.name)
    }
    filmstrip: set[str] = set()
    for directory in sorted(generated.glob("monitoring_visuals*")):
        if not directory.is_dir():
            continue
        for path in directory.rglob("IM-*-filmstrip.*"):
            match = re.match(r"^(IM-G(?:K|[1-8])-U\d+-L\d+)-", path.name)
            if match:
                filmstrip.add(match.group(1))
    return {
        "notation_chart": notation,
        "deformation_chart": deformation,
        "filmstrips": filmstrip,
    }


def build() -> tuple[dict, list[dict[str, int | str]]]:
    ratchet = load_ratchet()
    facts = prolog_facts()
    published = {fields[0] for fields in facts["published"]}
    expected_published = ratchet.get("floors", {}).get("published")
    if len(published) != expected_published:
        fail(
            f"published lesson mismatch: ratchet={expected_published}, "
            f"loaded Hermes stack={len(published)}"
        )

    hand: dict[str, set[str]] = defaultdict(set)
    compiled: dict[str, set[str]] = defaultdict(set)
    for lesson, _operation, kind in facts["hand"]:
        hand[lesson].add(kind)
    for lesson, _operation, kind in facts["compiled"]:
        compiled[lesson].add(kind)

    task_counts: Counter[str] = Counter()
    deformation_counts: Counter[str] = Counter()
    for lesson, classification in facts["task"]:
        task_counts[lesson] += 1
        if classification == "deformation":
            deformation_counts[lesson] += 1

    runtime_sets = {
        "published": published,
        "text_source": {x[0] for x in facts["text"]},
        "hand_strategy": set(hand),
        "compiled_strategy": set(compiled),
        "misconception_link": {x[0] for x in facts["misconception"]},
        "verbatim_context": {x[0] for x in facts["context"]},
        "vision_digest": {x[0] for x in facts["vision"]},
    }
    cross_checks = regex_cross_checks(runtime_sets)
    signatures = {x[0] for x in facts["signature"]}
    lesson_standards = {x[0] for x in facts["standard_lesson"]}
    unit_standards = {x[0] for x in facts["standard_unit"]}
    expected_lesson_standards = {
        lesson
        for lesson in published
        if (grade := lesson_parts(lesson)[0]) == "K" or int(grade) <= 5
    }
    expected_unit_standards = published - expected_lesson_standards
    missing_lesson_standards = expected_lesson_standards - lesson_standards
    missing_unit_standards = expected_unit_standards - unit_standards
    if missing_lesson_standards or missing_unit_standards:
        fail(
            "loaded standards do not support the K-5 lesson / 6-8 unit classification: "
            f"missing lesson={sorted(missing_lesson_standards)[:5]}, "
            f"missing unit={sorted(missing_unit_standards)[:5]}"
        )
    renders = render_ids()
    diagnostic_ready = set(ratchet.get("admitted_lessons", []))
    if not diagnostic_ready <= published:
        fail(
            "ratchet admitted lessons are absent from the published lesson set: "
            + ", ".join(sorted(diagnostic_ready - published))
        )

    encoded = set().union(
        runtime_sets["text_source"],
        set(hand),
        set(compiled),
        runtime_sets["misconception_link"],
        runtime_sets["verbatim_context"],
        set(task_counts),
        {x[0] for x in facts["vision_all"]},
    )
    extras = sorted(encoded - published, key=lesson_sort_key)
    if len(encoded) != 1317 or len(extras) != 9:
        fail(
            "encoded/published disagreement is not the expected nine-extra case: "
            f"encoded={len(encoded)}, extras={len(extras)}, ids={extras}"
        )

    rows = []
    for lesson in sorted(published, key=lesson_sort_key):
        grade, unit, number = lesson_parts(lesson)
        hand_atoms = hand[lesson]
        compiled_atoms = compiled[lesson]
        atoms = sorted(hand_atoms | compiled_atoms)
        if hand_atoms and compiled_atoms:
            strategy = "both"
        elif hand_atoms:
            strategy = "hand"
        elif compiled_atoms:
            strategy = "compiled"
        else:
            strategy = "none"
        row_renders = {key: lesson in ids for key, ids in renders.items()}
        rows.append(
            {
                "lesson": lesson,
                "grade": grade,
                "unit": unit,
                "lesson_number": number,
                "text_source": lesson in runtime_sets["text_source"],
                "standards_anchor": (
                    "lesson" if lesson in expected_lesson_standards else "unit"
                ),
                "strategy": strategy,
                "strategy_atoms": atoms,
                "automaton_resolves": bool(atoms)
                and all(atom in signatures for atom in atoms),
                "verbatim_context": lesson in runtime_sets["verbatim_context"],
                "task_instances": task_counts[lesson],
                "deformation_instances": deformation_counts[lesson],
                "misconception_link": lesson in runtime_sets["misconception_link"],
                "vision_digest": lesson in runtime_sets["vision_digest"],
                "renders": row_renders,
                "diagnostic_ready": lesson in diagnostic_ready,
            }
        )

    artifact = {
        "generated_by": GENERATED_HEADER,
        "schema": "hermes_im_coverage_v1",
        "source_ratchet_schema": ratchet["schema"],
        "published_lessons": rows,
        "encoded_but_unpublished": extras,
    }
    return artifact, cross_checks


def lesson_sort_key(lesson: str) -> tuple[int, int, int]:
    grade, unit, number = lesson_parts(lesson)
    return (0 if grade == "K" else int(grade), unit, number)


def count(rows: list[dict], predicate) -> int:
    return sum(1 for row in rows if predicate(row))


def summary_markdown(artifact: dict, cross_checks: list[dict[str, int | str]]) -> str:
    rows = artifact["published_lessons"]
    headers = [
        "Grade", "Lessons", "Text", "Std lesson", "Std unit", "Hand", "Compiled",
        "Strategy any", "Automaton resolves", "Context", "Task lessons", "Task instances",
        "Deformations", "Misconceptions", "Vision", "Notation", "Deformation chart",
        "Filmstrips", "Render any", "Diagnostic ready",
    ]

    def rollup(label: str, selected: list[dict]) -> list[str | int]:
        return [
            label,
            len(selected),
            count(selected, lambda r: r["text_source"]),
            count(selected, lambda r: r["standards_anchor"] == "lesson"),
            count(selected, lambda r: r["standards_anchor"] == "unit"),
            count(selected, lambda r: r["strategy"] in {"hand", "both"}),
            count(selected, lambda r: r["strategy"] in {"compiled", "both"}),
            count(selected, lambda r: r["strategy"] != "none"),
            count(selected, lambda r: r["automaton_resolves"]),
            count(selected, lambda r: r["verbatim_context"]),
            count(selected, lambda r: r["task_instances"] > 0),
            sum(r["task_instances"] for r in selected),
            sum(r["deformation_instances"] for r in selected),
            count(selected, lambda r: r["misconception_link"]),
            count(selected, lambda r: r["vision_digest"]),
            count(selected, lambda r: r["renders"]["notation_chart"]),
            count(selected, lambda r: r["renders"]["deformation_chart"]),
            count(selected, lambda r: r["renders"]["filmstrips"]),
            count(selected, lambda r: any(r["renders"].values())),
            count(selected, lambda r: r["diagnostic_ready"]),
        ]

    table_rows = []
    for grade in ["K", "1", "2", "3", "4", "5", "6", "7", "8"]:
        table_rows.append(rollup(grade, [row for row in rows if row["grade"] == grade]))
    table_rows.append(rollup("Total", rows))

    lines = [
        f"<!-- {GENERATED_HEADER} -->",
        "# IM lesson coverage",
        "",
        "| " + " | ".join(headers) + " |",
        "|" + "|".join(["---"] + ["---:"] * (len(headers) - 1)) + "|",
    ]
    lines.extend("| " + " | ".join(map(str, row)) + " |" for row in table_rows)
    lines.extend(
        [
            "",
            "## Method",
            "",
            "This inventory uses the 2026-07-20 published floor and admitted-lesson list from "
            "`lesson_evidence_ratchet.json` in the read-only `umedcta-formalization` checkout. "
            "Lesson IDs and coverage facts come from the Hermes lesson stack loaded through "
            "`paths.pl`. The builder also parses the generated fact files to cross-check the "
            "loaded counts.",
            "",
            "A lesson standard anchor is an exact lesson record for K–5 or a unit fallback for "
            "grades 6–8. Strategy counts distinguish hand clauses from compiled mappings; "
            "`automaton_resolves` requires at least one strategy atom and requires every atom "
            "to occur as an `action_automaton_signature/4` kind. Context means a "
            "`compiled_lesson_context/4` record. Task and deformation counts retain duplicate "
            "fact instances because each fact is a source-backed instance. Render fields record "
            "generated-file presence under `hermes/app/web/generated`; they do not assess "
            "pedagogical quality. A filmstrip is an `IM-*` filmstrip file under any "
            "`monitoring_visuals*` directory.",
            "",
            "The prior 740 lesson-standard survey count omitted Kindergarten. The live standard "
            "anchor file contains 139 exact Kindergarten lesson records, so this table reports "
            "879 lesson anchors. The prior 296 misconception count is reproduced by counting "
            "loaded clause heads; executing clause bodies alone reports fewer rows when a linked "
            "registry entry does not resolve.",
            "",
            "## SWI-Prolog and regex cross-check",
            "",
            "| Layer | SWI-Prolog | Regex |",
            "|---|---:|---:|",
        ]
    )
    for item in cross_checks:
        lines.append(f"| {item['layer']} | {item['swipl']} | {item['regex']} |")
    lines.append("")
    return "\n".join(lines)


def write_outputs(artifact: dict, cross_checks: list[dict[str, int | str]]) -> None:
    JSON_OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    json_text = json.dumps(artifact, ensure_ascii=False, indent=2) + "\n"
    summary_text = summary_markdown(artifact, cross_checks)
    JSON_OUTPUT.write_text(json_text, encoding="utf-8")
    SUMMARY_OUTPUT.write_text(summary_text, encoding="utf-8")


def main() -> None:
    artifact, cross_checks = build()
    write_outputs(artifact, cross_checks)
    print(
        f"wrote {len(artifact['published_lessons'])} published rows and "
        f"{len(artifact['encoded_but_unpublished'])} encoded extras"
    )
    for item in cross_checks:
        print(f"cross-check {item['layer']}: {item['swipl']} == {item['regex']}")


if __name__ == "__main__":
    main()
