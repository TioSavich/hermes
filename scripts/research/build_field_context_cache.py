#!/usr/bin/env python3
"""Build or merge checkpointed lesson field-context cache data.

Regenerate with:

    python3 scripts/research/build_field_context_cache.py

Run this whenever the lesson, strategy, misconception, standards, literature,
or inferential-strength knowledge base changes. The regular check suite compares
one fixed lesson against the live worker and fails when the artifact drifts.
The generator starts one SWI-Prolog process per invocation. That process applies
a 120-second limit to each lesson, streams each completed result back to Python,
and records failures instead of omitting entries. Python atomically writes one
partial JSON file per lesson before a deterministic merge.
"""

from __future__ import annotations

import argparse
import hashlib
import json
import re
import signal
import subprocess
import sys
import time
from pathlib import Path
from typing import Any, Callable


REPO = Path(__file__).resolve().parents[2]
COVERAGE_PATH = REPO / "curriculum/im/coverage/im_coverage.json"
OUTPUT_PATH = REPO / "curriculum/im/generated/field_context_cache.json"
DEFAULT_PARTIALS_PATH = REPO / ".bigred-output/field-context/partials"
BATCH_PATH = REPO / "scripts/research/field_context_batch.pl"
GENERATED_HEADER = (
    "generated by scripts/research/build_field_context_cache.py — do not hand-edit"
)
LESSON_CODE_RE = re.compile(r"^IM-G(K|[1-8])-U[0-9]+-L[0-9]+$")
GRADE_BANDS = {
    "K-2": frozenset({0, 1, 2}),
    "3-5": frozenset({3, 4, 5}),
    "6-8": frozenset({6, 7, 8}),
}
TIMEOUT_ERROR = "field_context_dict/2 exceeded 120 seconds"
STREAM_GOAL = f"""
with_output_to(user_error, load_runtime),
field_context_batch:json_read_dict(user_input, Request),
get_dict(lessons, Request, LessonStrings),
maplist(atom_string, LessonCodes, LessonStrings),
get_dict(jobs, Request, Jobs),
field_context_batch:prepare_batch_tables,
set_prolog_flag(cpu_count, Jobs),
flag(field_context_batch_completed, _Previous, 0),
field_context:lesson_readiness_inputs(_, _, _, _, _),
field_context:literature_summary_dict(_),
thread:concurrent_forall(
    member(Code, LessonCodes),
    (
        field_context_batch:field_context_entry(Code, Code-Initial),
        (
            get_dict(error, Initial, "{TIMEOUT_ERROR}")
        ->
            format(user_error, 'field_context cache: retrying ~w sequentially~n', [Code]),
            catch(
                call_with_time_limit(
                    120,
                    field_context_batch:field_context_result(Code, Result)
                ),
                Error,
                field_context_batch:field_context_error(Error, Result)
            )
        ;
            Result = Initial
        ),
        atom_string(Code, CodeString),
        with_mutex(
            field_context_stream_output,
            (
                field_context_batch:json_write_dict(
                    user_output,
                    _{{lesson_code: CodeString, context: Result}},
                    [width(0)]
                ),
                nl(user_output),
                flush_output(user_output)
            )
        )
    ),
    [threads(Jobs)]
)
""".replace("\n", " ")


def fail(message: str) -> "NoReturn":
    print(
        f"build_field_context_cache.py: {message}",
        file=sys.stderr,
        flush=True,
    )
    raise SystemExit(1)


def lesson_codes() -> list[str]:
    try:
        coverage = json.loads(COVERAGE_PATH.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError) as exc:
        fail(f"cannot read {COVERAGE_PATH}: {exc}")
    published = [row.get("lesson") for row in coverage.get("published_lessons", [])]
    extras = coverage.get("encoded_but_unpublished", [])
    codes = published + extras
    if not all(isinstance(code, str) and code for code in codes):
        fail(f"{COVERAGE_PATH} contains a missing or non-string lesson id")
    if len(codes) != len(set(codes)):
        fail(f"{COVERAGE_PATH} contains duplicate lesson ids")
    return sorted(codes)


def source_revision() -> str:
    """Read the checked-out short hash without invoking git.

    The implementation slice forbids git operations. Reading HEAD directly
    still gives the generated artifact the required source revision.
    """
    git_dir = REPO / ".git"
    try:
        head = (git_dir / "HEAD").read_text(encoding="utf-8").strip()
        if not head.startswith("ref: "):
            revision = head
        else:
            ref = head.removeprefix("ref: ")
            loose_ref = git_dir / ref
            if loose_ref.is_file():
                revision = loose_ref.read_text(encoding="utf-8").strip()
            else:
                revision = ""
                for line in (git_dir / "packed-refs").read_text(encoding="utf-8").splitlines():
                    if line and not line.startswith(("#", "^")):
                        candidate, candidate_ref = line.split(" ", 1)
                        if candidate_ref == ref:
                            revision = candidate
                            break
        if len(revision) < 12:
            raise ValueError("HEAD did not resolve to a commit hash")
        return revision[:12]
    except (OSError, ValueError) as exc:
        fail(f"cannot resolve source revision from {git_dir}: {exc}")


def compute_contexts(
    codes: list[str],
    *,
    jobs: int,
    on_context: Callable[[str, dict[str, Any]], None] | None = None,
) -> dict[str, dict[str, Any]]:
    """Compute codes in one SWI process and consume each result as it completes."""
    command = [
        "swipl",
        "--on-error=status",
        "--on-warning=status",
        "-q",
        "-l",
        "hermes_worker.pl",
        "-l",
        str(BATCH_PATH),
        "-g",
        STREAM_GOAL,
        "-t",
        "halt",
    ]
    try:
        process = subprocess.Popen(
            command,
            cwd=REPO,
            stdin=subprocess.PIPE,
            stdout=subprocess.PIPE,
            text=True,
            bufsize=1,
        )
    except FileNotFoundError:
        fail("swipl is required to build the field-context cache")

    if process.stdin is None or process.stdout is None:
        process.terminate()
        fail("cannot open pipes for the single-process SWI batch")

    contexts: dict[str, dict[str, Any]] = {}
    try:
        process.stdin.write(json.dumps({"lessons": codes, "jobs": jobs}))
        process.stdin.close()
        for line_number, line in enumerate(process.stdout, start=1):
            try:
                row = json.loads(line)
                code = row["lesson_code"]
                context = row["context"]
            except (json.JSONDecodeError, KeyError, TypeError) as exc:
                process.terminate()
                fail(f"SWI stream row {line_number} is invalid: {exc}")
            if (
                not isinstance(code, str)
                or code not in codes
                or not isinstance(context, dict)
            ):
                process.terminate()
                fail(f"SWI stream row {line_number} has an invalid lesson result")
            if code in contexts:
                process.terminate()
                fail(f"SWI stream returned duplicate lesson {code}")
            contexts[code] = context
            if on_context is not None:
                on_context(code, context)
    except KeyboardInterrupt:
        process.send_signal(signal.SIGINT)
        process.wait()
        print(
            "build_field_context_cache.py: interrupted; completed partials are preserved",
            file=sys.stderr,
            flush=True,
        )
        raise SystemExit(130)
    except BaseException:
        if process.poll() is None:
            process.terminate()
            process.wait()
        raise
    finally:
        process.stdout.close()

    return_code = process.wait()
    if return_code != 0:
        fail(f"single-process SWI batch failed with status {return_code}")
    if set(contexts) != set(codes):
        missing = sorted(set(codes) - set(contexts))
        unexpected = sorted(set(contexts) - set(codes))
        fail(f"batch lesson mismatch; missing={missing}, unexpected={unexpected}")
    if not all(isinstance(value, dict) for value in contexts.values()):
        fail("single-process SWI batch returned a non-object lesson value")
    return contexts


def serialized_artifact(contexts: dict[str, dict[str, Any]]) -> bytes:
    artifact = {
        "generated_by": GENERATED_HEADER,
        "schema": "hermes_field_context_cache_v1",
        "source_revision": source_revision(),
        "field_contexts": contexts,
    }
    return (json.dumps(
        artifact,
        ensure_ascii=False,
        indent=2,
        sort_keys=True,
    ) + "\n").encode("utf-8")


def partial_path(partials_dir: Path, code: str) -> Path:
    if LESSON_CODE_RE.fullmatch(code) is None:
        fail(f"cannot form a partial path for invalid lesson id {code!r}")
    return partials_dir / f"{code}.json"


def serialized_partial(code: str, context: dict[str, Any]) -> bytes:
    return (json.dumps(
        {"lesson_code": code, "context": context},
        ensure_ascii=False,
        indent=2,
        sort_keys=True,
    ) + "\n").encode("utf-8")


def write_atomic(path: Path, data: bytes) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    temporary = path.with_suffix(path.suffix + ".tmp")
    temporary.write_bytes(data)
    temporary.replace(path)


def read_partial(path: Path) -> tuple[str, dict[str, Any]]:
    try:
        row = json.loads(path.read_text(encoding="utf-8"))
        code = row["lesson_code"]
        context = row["context"]
    except (OSError, json.JSONDecodeError, KeyError, TypeError) as exc:
        fail(f"cannot read partial {path}: {exc}")
    if (
        not isinstance(code, str)
        or LESSON_CODE_RE.fullmatch(code) is None
        or path.stem != code
        or not isinstance(context, dict)
    ):
        fail(f"partial {path} has an invalid lesson result")
    return code, context


def collect_partials(
    codes: list[str],
    *,
    partials_dir: Path,
    resume: bool,
    jobs: int,
) -> dict[str, dict[str, Any]]:
    partials_dir.mkdir(parents=True, exist_ok=True)
    existing: dict[str, dict[str, Any]] = {}
    if resume:
        for code in codes:
            path = partial_path(partials_dir, code)
            if path.is_file():
                existing_code, context = read_partial(path)
                if existing_code != code:
                    fail(f"partial {path} identifies lesson {existing_code}")
                existing[code] = context
    pending = [code for code in codes if code not in existing]
    print(
        f"checkpoint selection: lessons={len(codes)} resumed={len(existing)} "
        f"pending={len(pending)} jobs={jobs} partials={partials_dir}",
        flush=True,
    )
    if not pending:
        return existing

    def checkpoint(code: str, context: dict[str, Any]) -> None:
        path = partial_path(partials_dir, code)
        write_atomic(path, serialized_partial(code, context))
        print(
            f"checkpointed {code}: {path}",
            flush=True,
        )

    computed = compute_contexts(pending, jobs=jobs, on_context=checkpoint)
    return {**existing, **computed}


def contexts_from_partials(partials_dir: Path) -> dict[str, dict[str, Any]]:
    if not partials_dir.is_dir():
        fail(f"partial directory does not exist: {partials_dir}")
    contexts: dict[str, dict[str, Any]] = {}
    for path in sorted(partials_dir.glob("*.json")):
        code, context = read_partial(path)
        if code in contexts:
            fail(f"duplicate partial for lesson {code}")
        contexts[code] = context
    if not contexts:
        fail(f"partial directory contains no lesson JSON files: {partials_dir}")
    return contexts


def write_artifact(
    contexts: dict[str, dict[str, Any]],
    *,
    output_path: Path,
    started: float,
) -> bytes:
    data = serialized_artifact(contexts)
    write_atomic(output_path, data)
    errors = {
        code: context["error"]
        for code, context in contexts.items()
        if "error" in context
    }
    digest = hashlib.sha256(data).hexdigest()
    elapsed = time.monotonic() - started
    try:
        display_path = output_path.relative_to(REPO)
    except ValueError:
        display_path = output_path
    print(
        f"wrote {display_path}: entries={len(contexts)} "
        f"errors={len(errors)} wall_seconds={elapsed:.3f} sha256={digest}",
        flush=True,
    )
    for code, error in sorted(errors.items()):
        print(f"ERROR {code}: {error}", flush=True)
    return data


def existing_contexts() -> dict[str, dict[str, Any]]:
    try:
        artifact = json.loads(OUTPUT_PATH.read_text(encoding="utf-8"))
        contexts = artifact["field_contexts"]
    except (OSError, json.JSONDecodeError, KeyError, TypeError) as exc:
        fail(f"cannot read an existing cache for --retry-errors: {exc}")
    if not isinstance(contexts, dict):
        fail("existing cache field_contexts is not an object")
    return contexts


def lesson_grade(code: str) -> int:
    match = LESSON_CODE_RE.fullmatch(code)
    if match is None:
        fail(f"invalid lesson id {code!r}")
    grade = match.group(1)
    return 0 if grade == "K" else int(grade)


def selected_codes(
    inventory: list[str],
    *,
    lessons: list[str],
    grade_band: str | None,
    limit: int,
) -> list[str]:
    if lessons:
        unknown = sorted(set(lessons) - set(inventory))
        if unknown:
            fail(f"requested lessons are not in the coverage inventory: {unknown}")
        codes = sorted(set(lessons))
    else:
        codes = inventory
    if grade_band is not None:
        grades = GRADE_BANDS[grade_band]
        codes = [code for code in codes if lesson_grade(code) in grades]
    if limit:
        codes = codes[:limit]
    if not codes:
        fail("lesson selection is empty")
    return codes


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--partials-dir",
        type=Path,
        default=DEFAULT_PARTIALS_PATH,
        help=f"checkpoint directory (default: {DEFAULT_PARTIALS_PATH})",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=OUTPUT_PATH,
        help=f"merged artifact path (default: {OUTPUT_PATH})",
    )
    parser.add_argument(
        "--resume",
        action="store_true",
        help="skip selected lessons with valid existing partials",
    )
    mode = parser.add_mutually_exclusive_group()
    mode.add_argument(
        "--collect-only",
        action="store_true",
        help="write selected lesson partials without merging",
    )
    mode.add_argument(
        "--merge-only",
        action="store_true",
        help="merge existing partials without starting SWI-Prolog",
    )
    parser.add_argument(
        "--require-complete",
        action="store_true",
        help="refuse to merge unless partials cover the full lesson inventory",
    )
    parser.add_argument(
        "--grade-band",
        choices=sorted(GRADE_BANDS),
        help="select one grade band",
    )
    parser.add_argument(
        "--lesson",
        action="append",
        default=[],
        help="select one lesson id; repeat for multiple lessons",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=0,
        help="select at most N lessons after other filters (0 means all)",
    )
    parser.add_argument(
        "--jobs",
        type=int,
        default=8,
        help="SWI worker threads inside the single process (default: 8)",
    )
    parser.add_argument(
        "--retry-errors",
        action="store_true",
        help="recompute only explicit error entries in the existing artifact",
    )
    args = parser.parse_args(argv)
    if args.limit < 0:
        parser.error("--limit must be nonnegative")
    if args.jobs < 1:
        parser.error("--jobs must be positive")
    if args.merge_only and (
        args.resume
        or args.retry_errors
        or args.grade_band is not None
        or args.lesson
        or args.limit
    ):
        parser.error(
            "--merge-only cannot be combined with collection selection options"
        )
    if args.retry_errors and (
        args.grade_band is not None or args.lesson or args.limit
    ):
        parser.error("--retry-errors cannot be combined with lesson filters")
    if args.collect_only and args.require_complete:
        parser.error("--require-complete applies only to a merge")

    started = time.monotonic()
    inventory = lesson_codes()

    if args.merge_only:
        contexts = contexts_from_partials(args.partials_dir)
    elif args.retry_errors:
        existing = existing_contexts()
        if set(existing) != set(inventory):
            fail("existing cache lesson ids do not match the coverage inventory")
        codes = sorted(
            code for code, context in existing.items() if "error" in context
        )
        if not codes:
            print("existing cache has no explicit error entries", flush=True)
            return 0
        replacements = collect_partials(
            codes,
            partials_dir=args.partials_dir,
            resume=args.resume,
            jobs=args.jobs,
        )
        contexts = {**existing, **replacements}
    else:
        codes = selected_codes(
            inventory,
            lessons=args.lesson,
            grade_band=args.grade_band,
            limit=args.limit,
        )
        collect_partials(
            codes,
            partials_dir=args.partials_dir,
            resume=args.resume,
            jobs=args.jobs,
        )
        if args.collect_only:
            return 0
        contexts = contexts_from_partials(args.partials_dir)

    if args.require_complete and set(contexts) != set(inventory):
        missing = sorted(set(inventory) - set(contexts))
        unexpected = sorted(set(contexts) - set(inventory))
        fail(
            "partial lesson mismatch for complete merge; "
            f"missing={missing}, unexpected={unexpected}"
        )
    write_artifact(contexts, output_path=args.output, started=started)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
