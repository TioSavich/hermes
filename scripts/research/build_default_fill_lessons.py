#!/usr/bin/env python3
"""Regenerate curriculum/im/generated/default_fill_lessons.pl from the
coverage table: the lessons whose strategy atoms include
unit_fraction_partition or unit_fraction_iteration. The deformation chart
reads this fact table instead of scanning the strategy union at runtime,
which costs minutes in the full worker image."""
import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[2]
d = json.load(open(ROOT / "curriculum/im/coverage/im_coverage.json"))
kinds = {"unit_fraction_partition", "unit_fraction_iteration"}
codes = sorted(r["lesson"] for r in d["published_lessons"]
               if set(r.get("strategy_atoms", [])) & kinds)
out = ROOT / "curriculum/im/generated/default_fill_lessons.pl"
lines = [
    "/** <module> Generated: lessons eligible for the default deformation chart",
    " *",
    " * Generated by scripts/research/build_default_fill_lessons.py from the",
    " * coverage table (lessons whose strategy atoms include",
    " * unit_fraction_partition or unit_fraction_iteration). Do not hand-edit;",
    " * regenerate after the coverage table changes.",
    " */",
    ":- module(default_fill_lessons, [default_fill_lesson/1]).",
    "",
] + [f"default_fill_lesson('{c}')." for c in codes]
out.write_text("\n".join(lines) + "\n")
print(f"wrote {len(codes)} default_fill_lesson facts")
