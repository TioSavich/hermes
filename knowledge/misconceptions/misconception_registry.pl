    ensure_batch_rows_loaded,
    cached_batch_row(Row),
    (   cached_batch_row_source(Row, Path)
    ->  true
    ;   Path = unknown_misconception_batch_csv
    ),
    csv_row_witness(misconception_batch, Path, 16, Row, Witness).


