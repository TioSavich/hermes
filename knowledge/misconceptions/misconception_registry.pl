           ( assertz(cached_batch_row(Row)),
             assertz(cached_batch_row_source(Row, Path))
           )).


csv_rows_with_arity(Path, ExpectedArity, Rows) :-
    csv_rows_with_arity_witness(Path, ExpectedArity, Rows, _).


csv_rows_with_arity_witness(Path, ExpectedArity, Rows, Witness) :-
    csv_read_file(Path, Rows, [functor(row), convert(false)]),
    (   forall(member(Row, Rows), functor(Row, row, ExpectedArity))
    ->  length(Rows, RowCount),
        Witness = _{ kind: csv_rows_with_arity,
                     path: Path,
                     expected_arity: ExpectedArity,
                     row_count: RowCount }
    ;   domain_error(csv_row_arity(ExpectedArity), Path)
    ).


csv_row_witness(SourceKind, Path, ExpectedArity, Row, Witness) :-
    functor(Row, row, RowArity),
    Witness = _{ kind: misconception_registry_csv_row,
                 source_kind: SourceKind,
                 path: Path,
                 expected_arity: ExpectedArity,
                 row_arity: RowArity,
                 row: Row }.


