%!  misconception_registry_entry(?Name, ?TargetOperation, ?Citation,
%!                               ?CommitmentMade, ?EntitlementLacked) is nondet.
%
%   True for literature-attested misconceptions that are also generated by
%   the strategy deformation registry.
misconception_registry_entry(Name, Operation, Citation, Commitment, Entitlement) :-
    attested_misconception(Name, Operation, Productive, Family, Citation),
    action_automaton_pair(Operation, Productive, Name, Family),
    Commitment = deformed_action(Productive, Name, Family),
    Entitlement = strategy(Operation, Productive),
    misconception(Commitment).
misconception_registry_entry(Name, Operation, Citation, Commitment, Entitlement) :-
    harness_backed_misconception(Source,
                                 _Domain,
                                 Name,
                                 Rule,
                                 Input,
                                 Expected,
                                 Operation,
                                 Citation),
    rule_result(Rule, Input, Got),
    Got \=@= Expected,
    Commitment = result_of(Name, Source, Got),
    Entitlement = result_of(Name, Source, Expected).
misconception_registry_entry(Name, Operation, Citation, Commitment, Entitlement) :-
    harness_backed_misconception(Source,
                                 _Domain,
                                 Name,
                                 Rule,
                                 Input,
                                 Expected,
                                 Operation,
                                 Citation),
    \+ ( rule_result(Rule, Input, Got),
         Got \=@= Expected
       ),
    Commitment = documented_batch_misconception(Name, Source, Input, Rule),
    Entitlement = expected_result(Name, Source, Expected).
misconception_registry_entry(Name, Operation, citation(BibtexKey, Note), Commitment, Entitlement) :-
    test_harness:arith_misconception(Source, Domain, Name, skip, Input, Expected),
    Source = db_row(_),
    batch_row_for_source(Source, Domain, Operation, BibtexKey, Note),
    BibtexKey \== '',
    Commitment = documented_batch_misconception(Name, Source, Input, skip),
    Entitlement = expected_result(Name, Source, Expected).
misconception_registry_entry(Name, Operation, citation(BibtexKey, Note), Commitment, Entitlement) :-
    misconception_batch_row(row(RowId,
                                ErrorDescription,
                                _Example,
                                Domain,
                                Topic,
                                _Subtopic,
                                _Population,
                                _GradeBand,
                                BibtexKey,
                                _Authors,
                                _Year,
                                _Journal,
                                _Orientation,
                                _Salience,
                                _PageRefs,
                                _LocalPdfPath)),
    BibtexKey \== '',
    row_id_matches(RowId, Id),
    \+ test_harness:arith_misconception(db_row(Id), _, _, _, _, _),
    normalize_operation(Topic, Domain, Operation),
    batch_row_name(Id, Name),
    text_to_string(ErrorDescription, Note),
    Commitment = documented_batch_misconception(Name, db_row(Id), Note, Operation),
    Entitlement = expected_mathematical_control(Name, db_row(Id), Operation).


%!  misconception_term(?Name, ?Commitment) is nondet.
misconception_term(Name, Commitment) :-
    misconception_registry_entry(Name, _, _, Commitment, _).


%!  incompatibility_with(?Move, ?Conflict) is nondet.
%
%   Reports the strategy-level commitment a deformation cannot sustain. This
%   predicate is intentionally grounded in registered productive/deformation
%   pairs, so it fails when the action registry has no conflicting move.
incompatibility_with(Move, Conflict) :-
    incompatibility_with_witness(Move, Conflict, _).


%!  incompatibility_with_witness(?Move, ?Conflict, -Witness) is nondet.
%
%   True when the registry can derive an incompatibility and explain which
%   finite registry surface proved it. This is a closed-world finite registry
%   check over loaded action automata, cited misconception rows, harness rows,
%   and geometry misconception facts; it is not an open-ended semantic
%   incompatibility prover.
incompatibility_with_witness(Name,
                             strategy(Operation, Productive),
                             Witness) :-
    atom(Name),
    action_automaton_pair(Operation, Productive, Name, Family),
    registry_incompatibility_witness(
        action_automaton_deformation,
        Name,
        strategy(Operation, Productive),
        _{ operation: Operation,
           productive: Productive,
           deformation: Name,
           family: Family },
        Witness).
incompatibility_with_witness(Name, Entitlement, Witness) :-
    atom(Name),
    registered_name_entitlement_witness(Name, Entitlement, Evidence),
    registry_incompatibility_witness(
        registered_name_entitlement,
        Name,
        Entitlement,
        Evidence,
        Witness).
incompatibility_with_witness(Name, Entitlement, Witness) :-
    atom(Name),
    misconception_registry_entry(Name, Operation, Citation, Commitment, Entitlement),
    Commitment \=@= Entitlement,
    citation_witness(Citation, CitationWitness),
    registry_incompatibility_witness(
        registry_entry_lacked_entitlement,
        Name,
        Entitlement,
        _{ operation: Operation,
           commitment: Commitment,
           entitlement: Entitlement,
           citation: CitationWitness },
        Witness0),
    Witness = Witness0.put(_{name: Name,
                             operation: Operation,
                             citation: CitationWitness}).
incompatibility_with_witness(misconception(Name),
