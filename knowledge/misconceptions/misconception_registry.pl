rule_result(Module:LocalName, Input, Got) :-
    !,
    Goal =.. [LocalName, Input, Got],
    safe_call_rule(Module:Goal).
rule_result(LocalName, Input, Got) :-
    Goal =.. [LocalName, Input, Got],
    safe_call_rule(Goal).


safe_call_rule(Goal) :-
    catch(call_with_inference_limit(Goal, 10_000, Status),
          _Error,
          fail),
    Status \== inference_limit_exceeded.


normalize_operation(Topic, Domain, Operation) :-
    text_to_string(Topic, TopicString0),
    (   TopicString0 == ""
    ->  text_to_string(Domain, TopicString)
    ;   TopicString = TopicString0
    ),
    string_lower(TopicString, Lower),
    split_string(Lower, " /-,.;:?!()[]\"'", " /-,.;:?!()[]\"'", Parts),
    exclude(=(""), Parts, NonEmptyParts),
    (   NonEmptyParts == []
    ->  Operation = Domain
    ;   atomic_list_concat(NonEmptyParts, '_', Operation)
    ).


text_to_string(Text, String) :-
    (   string(Text)
    ->  String = Text
    ;   atom(Text)
    ->  atom_string(Text, String)
    ;   term_string(Text, String)
    ).
